    public void login(){
        String text = jTextUser.getText();
        if(!text.isEmpty()){
            Date data = new Date();
            logado = new User(text,fmt.format(data));
            LL = servidor.addClient(logado);
            TelaTexto TT = new TelaTexto(servidor,logado,LL);
            TT.setVisible(true);
            this.dispose();
        }else
            JOptionPane.showMessageDialog(null, "Insira um usu√°rio");
    }
    
